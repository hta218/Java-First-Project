    private void loadDataIntoTable_NhaCC() throws ClassNotFoundException, SQLException {
        //nccởi tạo lại model, xóa hết cột cũ dòng cũ đi
        tableModel = new DefaultTableModel();
        //tạo cột
        tableModel.addColumn("Supplier No.");
        tableModel.addColumn("Supplier Name");
        tableModel.addColumn("Phone Number");
        tableModel.addColumn("Email");
        tableModel.addColumn("Address");
        NhaCungCapDAO nhaCungCapDAO = new NhaCungCapDAO();
        //lấy ra toàn bộ vai trò
        List<NhaCungCap> nhaCungCapList = nhaCungCapDAO.selectAll();
        for (NhaCungCap ncc : nhaCungCapList) {
            Vector vector = new Vector();
            vector.add(ncc.getMaNhaCC());
            vector.add(ncc.getTenNhaCC());
            vector.add(ncc.getSdt());
            vector.add(ncc.getEmail());
            vector.add(ncc.getDiaChi());
            //tạo hàng
            tableModel.addRow(vector);
        }
        //đưa dữ liệu từ model vào bảng
        tab_NhaCungCap.setModel(tableModel);
    }

