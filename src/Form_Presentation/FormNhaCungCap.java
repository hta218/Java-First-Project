    public boolean soSanh(int row) {
        Integer maNhaCungCap = (Integer) tab_NhaCungCap.getValueAt(row, 0);
        NhaCungCap ncc = (new NhaCungCapDAO()).getNhaCungCapByID(maNhaCungCap);
        String tenNhaCungCap = txt_TenNCC.getText().trim();
        Integer sdt = txt_SDT.getText().trim().equals(ncc.getSdt().toString())
                ? Integer.parseInt(txt_SDT.getText().trim()) : (new Random()).nextInt();
        String email = txt_Email.getText().trim();
        String diaChi = txt_DiaChi.getText().trim();
        if (ncc.getTenNhaCC().equals(tenNhaCungCap) && ncc.getSdt().equals(sdt) && ncc.getDiaChi().equals(diaChi)
                && ncc.getEmail().trim().equals(email)) {
            return true;
        }
        return false;
    }
