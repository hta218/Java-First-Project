    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed

        try {
            //kiêm tra các thông tin bắt buộc nhập là quyền và tên vai trò
            if (txt_MaNhaCC.getText().isEmpty() || txt_TenNCC.getText().isEmpty() || txt_SDT.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please fill in all required fields(*) in the Information box");
                lbl_maNhaCC_Hint.setVisible(false);
                lbl_sdt_Hint.setVisible(false);
                return;
            }

            while (true) {
                try {
                    while (true) {
                        try {
                            Integer.parseInt(txt_MaNhaCC.getText().trim());
                            lbl_maNhaCC_Hint.setVisible(false);
                            break;
                        } catch (NumberFormatException e) {
                            lbl_maNhaCC_Hint.setVisible(true);
                            break;
                        }
                    }
                    while (true) {
                        try {
                            Integer.parseInt(txt_SDT.getText().trim());
                            lbl_sdt_Hint.setVisible(false);
                            break;
                        } catch (NumberFormatException e) {
                            lbl_sdt_Hint.setVisible(true);
                            break;
                        }
                    }
                    Integer.parseInt(txt_MaNhaCC.getText().trim());
                    Integer.parseInt(txt_SDT.getText().trim());
                    break;
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Invalid!! \n"
                            + "Some fields are not allowed to contain letters or special characters");
                    return;
                }
            }
            NhaCungCapDAO nhaCungCapDAO = new NhaCungCapDAO();
            if (nhaCungCapDAO.getListMaNhaCungCap().contains(Integer.parseInt(txt_MaNhaCC.getText().trim()))) {
                JOptionPane.showMessageDialog(this, "Supplier Number already exists!\n"
                        + "Re-Enter the Supplier Number!!");
                txt_MaNhaCC.setText("");
                return;
            }
            //lấy giá trị nhập trên form truyền vào đôi tượng
            NhaCungCap ncc = new NhaCungCap();
            ncc.setMaNhaCC(Integer.parseInt(txt_MaNhaCC.getText()));
            ncc.setTenNhaCC(txt_TenNCC.getText());
            ncc.setSdt(Integer.parseInt(txt_SDT.getText()));
            ncc.setEmail(txt_Email.getText());
            ncc.setDiaChi(txt_DiaChi.getText());
            //thực hiện thêm vào DB
            nhaCungCapDAO.insert(ncc);

            //reset form
            xoaTrang();
            xoaTrangSearch();
            //load lại dữ liệu lên lưới
            loadDataIntoTable_NhaCC();
            //nếu thành công thì thông báo
            JOptionPane.showMessageDialog(this, "Successfully add new Supplier !");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FormNhaCungCap.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(FormNhaCungCap.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnThemActionPerformed

