    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        xoaTrang();
        lbl_sdt_Search_Hint.setVisible(false);
        lbl_maNhaCC_Search_Hint.setVisible(false);
        //khởi tạo lại model, xóa hết cột cũ dòng cũ đi
        tableModel = new DefaultTableModel();
        //tạo cột
        tableModel.addColumn("Supplier No.");
        tableModel.addColumn("Supplier Name");
        tableModel.addColumn("Phone Number");
        tableModel.addColumn("Email");
        tableModel.addColumn("Address");
        NhaCungCapDAO nhaCungCapDAO = new NhaCungCapDAO();
        //lấy ra thông tin tìm kiếm
        NhaCungCap ncc = new NhaCungCap();
        if (txt_Search_TenNhaCC.getText().trim().equals("") && txt_Search_NCC_ID.getText().trim().equals("")
                && txt_Search_sdt.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter information into the search box!");
            return;
        }
        if (!txt_Search_NCC_ID.getText().trim().equals("")) {
            while (true) {
                try {
                    Integer.parseInt(txt_Search_NCC_ID.getText().trim());
                    lbl_maNhaCC_Search_Hint.setVisible(false);
                    break;
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Invalid!! \n"
                            + "Some fields are not allowed to contain letters or special characters");
                    lbl_maNhaCC_Search_Hint.setVisible(true);
                    return;
                }
            }
        }
        if (!txt_Search_sdt.getText().trim().equals("")) {
            while (true) {
                try {
                    Integer.parseInt(txt_Search_sdt.getText().trim());
                    lbl_sdt_Search_Hint.setVisible(false);
                    break;
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Invalid!! \n"
                            + "Some fields are not allowed to contain letters or special characters");
                    lbl_sdt_Search_Hint.setVisible(true);
                    return;
                }
            }
        }
        ncc.setTenNhaCC(txt_Search_TenNhaCC.getText().trim());
        if (!txt_Search_NCC_ID.getText().trim().equals("")) {
            ncc.setMaNhaCC(Integer.parseInt(txt_Search_NCC_ID.getText().trim()));
        }
        if (!txt_Search_sdt.getText().trim().equals("")) {
            ncc.setSdt(Integer.parseInt(txt_Search_sdt.getText().trim()));
        }

        List<NhaCungCap> nhaCungCapList = nhaCungCapDAO.search(ncc);
        for (NhaCungCap ncco : nhaCungCapList) {
            Vector vector = new Vector();
            vector.add(ncco.getMaNhaCC());
            vector.add(ncco.getTenNhaCC());
            vector.add(ncco.getSdt());
            vector.add(ncco.getEmail());
            vector.add(ncco.getDiaChi());
            //tạo hàng
            tableModel.addRow(vector);
        }
        //đưa dữ liệu từ model vào bảng
        tab_NhaCungCap.setModel(tableModel);

    }//GEN-LAST:event_btnTimKiemActionPerformed

