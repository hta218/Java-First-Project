package Object_BusinessLogic;

/**
 *
 * @author Huynh
 */
public class ChiTietHoaDon {

    private Integer soHoaDon;
    private String maSanPham;
    private String tenSanPham;
    private String donGiaSanPham;
    private String donViTinh;
    private Integer soLuong;
    private Integer thanhTien;

    public Integer getSoHoaDon() {
        return soHoaDon;
    }

    public void setSoHoaDon(Integer soHoaDon) {
        this.soHoaDon = soHoaDon;
    }

    public String getMaSanPham() {
        return maSanPham;
    }

    public void setMaSanPham(String maSanPham) {
        this.maSanPham = maSanPham;
    }

    public String getTenSanPham() {
        return tenSanPham;
    }

    public void setTenSanPham(String tenSanPham) {
        this.tenSanPham = tenSanPham;
    }

    public String getDonGiaSanPham() {
        return donGiaSanPham;
    }

    public void setDonGiaSanPham(String donGiaSanPham) {
        this.donGiaSanPham = donGiaSanPham;
    }

    public String getDonViTinh() {
        return donViTinh;
    }

    public void setDonViTinh(String donViTinh) {
        this.donViTinh = donViTinh;
    }

    public Integer getSoLuong() {
        return soLuong;
    }

    public void setSoLuong(Integer soLuong) {
        this.soLuong = soLuong;
    }

    public Integer getThanhTien() {
        return thanhTien;
    }

    public void setThanhTien(Integer thanhTien) {
        this.thanhTien = thanhTien;
    }

}
